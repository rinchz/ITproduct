def render(text: str, *, gap: str = "  ") -> str:
    normalized = text.upper()
    rows: list[list[str]] = [[] for _ in range(FONT_HEIGHT)]

    for ch in normalized:
        glyph = FONT.get(ch, FONT["?"])
        if len(glyph) != FONT_HEIGHT:
            raise ValueError(f"Invalid glyph height for {ch!r}: {len(glyph)}")
        for i in range(FONT_HEIGHT):
            rows[i].append(glyph[i])

    return "\n".join(gap.join(parts).rstrip() for parts in rows)


def main() -> None:
    s = input("Введите строку: ")
    if not s:
        print("(пустая строка)")
        return
    print(render(s))


if __name__ == "__main__":
    main()
